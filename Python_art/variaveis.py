# Variáveis são como caixinhas que guardam informação.

# Existem vários tipos de variáveis, cada uma preparada para guardar um tipo diferente de informação

# variável de texto -> String:
# Strings são variáveis que guardam letras, números e caracteres e
# são usadas para gaurdar textos.
texto = "Oi, eu sou uma string"

# variável de número inteiro -> int:
# Integers são variáveis que guardam números inteiros.
# podendo ser negativos ou positivos.
numero_inteiro = 5

# variável de número quebrado -> float:
# Floats são variáveis que guardam números "quebrados"(Reais).
# podendo ser negativos ou positivos.
numero_real = 5.5

# variável de verdadeiro ou falso -> Boolean:
# Variáveis booleanas guardam valores que podem ser verdadeiro ou falso.
verdadeiro_falso = True


